package thecalculator;

public class TheCalculator
{
    public static int Add(int a,int b)
    {
        return a+b;
    }

    public static int Sub(int a,int b)
    {
        return a-b;
    }

    public static int Mul(int a,int b)
    {
        return a*b;
    }

    public int Div(int a,int b)
    {
        return a/b;
    }
}